package eu.qwan.vender;

public class CanContainer {
	private Can type;
	private int price;
	private int amount;

	public Can getType() {
		return type;
	}

	public void setType(Can type) {
		this.type = type;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}
}
